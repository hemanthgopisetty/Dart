void main(List<String> args) {
   print(2+6);
   print(10-2);
   print(2*4);
   print(24/3);//double divison
   print(24~/3);//integer divison
   print(24%3);//remainder calculation Modulo %
   print(24+(2+1));//First 2+1 will executed into value 3 thenn it will get added to 24 
}

